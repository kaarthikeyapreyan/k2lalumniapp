// Helper to populate user data in events
export const populateEventData = (events: Event[], profiles: typeof mockProfiles) => {
  const profileMap = new Map(profiles.map(p => [p.id, p]));
  
  events.forEach(event => {
    event.attendees = event.attendees.map(attendee => ({
      ...attendee,
      user: profileMap.get(attendee.userId),
    }));
  });
};

populateEventData(mockEvents, mockProfiles);
