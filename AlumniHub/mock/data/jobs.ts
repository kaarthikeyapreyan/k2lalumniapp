// Helper to populate user data in jobs
export const populateJobData = (jobs: Job[], profiles: typeof mockProfiles) => {
  const profileMap = new Map(profiles.map(p => [p.id, p]));
  
  jobs.forEach(job => {
    job.poster = profileMap.get(job.postedBy);
    job.applications = job.applications.map(app => ({
      ...app,
      applicant: profileMap.get(app.applicantId),
    }));
  });
};

populateJobData(mockJobs, mockProfiles);
